S = 0
N = int(input("Введите желаемое количество билетов "))
for i in range(1, N+1):
    P = int(input("Введите возраст посетителя "))
    if P < 18:
        S += 0
    elif 18 <= P <= 25:
        S += 990
    elif P > 25:
        S += 1390
if N > 3:
    S = S - S/10
    print("Сумма к оплате со скидкой 10% - ", S, "руб")
else:
    print("Сумма к оплате - ", S, "руб")